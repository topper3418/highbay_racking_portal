#NULL: Represents a null or missing value.

#INTEGER: Represents a signed integer value, which can be up to 8 bytes in size.

#REAL: Represents a floating-point value, which can be up to 8 bytes in size.

#TEXT: Represents a string of text, which can be up to 2^31-1 bytes in size.

#BLOB: Represents a binary large object, which can be up to 2^31-1 bytes in size.